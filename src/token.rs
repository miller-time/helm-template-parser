#[derive(Clone, Copy, Debug, PartialEq)]
pub enum Token {
    Space,
    Letter(char),
    Number(u32),
    Dash,
    Colon,
    Slash,
    Pipe,
    Dot,
    Comma,
    SingleQuote,
    DoubleQuote,
    Asterisk,
    Equals,
    Percent,
    Newline,
    LeftParen,
    RightParen,
    LeftBracket,
    RightBracket,
    LeftBrace,
    RightBrace,
    Dollar,
}
